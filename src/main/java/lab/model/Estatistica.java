package lab.model;

public class Estatistica {
	private int homem;
	private int mulher;
	private int maior;
	private int menor;
	
	
	public int getHomem() {
		return homem;
	}
	public void setHomem(int homem) {
		this.homem = homem;
	}
	public int getMulher() {
		return mulher;
	}
	public void setMulher(int mulher) {
		this.mulher = mulher;
	}
	public int getMaior() {
		return maior;
	}
	public void setMaior(int maior) {
		this.maior = maior;
	}
	public int getMenor() {
		return menor;
	}
	public void setMenor(int menor) {
		this.menor = menor;
	}
	
	
}
